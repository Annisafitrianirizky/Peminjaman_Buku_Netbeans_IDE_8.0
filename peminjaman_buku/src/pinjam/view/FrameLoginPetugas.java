    private void bLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bLoginActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "SELECT * FROM tblpetugas WHERE "
                    + "nama_petugas='" + tUser.getText() + "' AND password='" + String.valueOf((tPass.getText()))+"'";
            Statement st = connection.createStatement();
            ResultSet rs = st.executeQuery(sql);
            if(rs.next()){
                if(tUser.getText().equals(rs.getString("nama_petugas"))
                    && String.valueOf((tPass.getText())).equals(rs.getString("password"))){
                    JOptionPane.showMessageDialog(this, "Hallo "
                    + rs.getString("nama_petugas"));
                    this.dispose();
                    FrameMenuPetugas x = new FrameMenuPetugas();
                    this.setVisible(false);
                    x.setVisible(rootPaneCheckingEnabled);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Nama petugas atau password salah");
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_bLoginActionPerformed

